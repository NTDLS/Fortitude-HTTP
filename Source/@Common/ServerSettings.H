typedef struct _Tag_Server_User_Item {
	char *Username;
	char *Password;
	char *Description;
	bool Enabled;

#ifndef HTTP_SERVICE
	bool ParentEnabled;
#endif
} SERVERUSER, *LPSERVERUSER;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

typedef struct _Tag_Server_Users {
	int Count;
	SERVERUSER *Items;
	bool AllowRemoteManagement;
} SERVERUSERS, *LPSERVERUSERS;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

typedef struct _Tag_Server_Processor {
	DWORD_PTR AffinityMask;
	bool ProcessBoost;
	bool ThreadBoost;
} SERVERPROCESSOR, *LPSERVERPROCESSOR;

typedef struct _Tag_Server_Advanced {
	bool OptimizeForThroughput;
	bool ForceSocketReuse;
} SERVERADVANCED, *LPSERVERADVANCED;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

typedef struct _tag_Server_Settings {
	SERVERUSERS Users;
	SERVERPROCESSOR Processor;
	SERVERADVANCED Advanced;
} SERVERSETTINGS, *LPSERVERSETTINGS;

